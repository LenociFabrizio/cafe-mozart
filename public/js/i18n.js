Object.keys(EXTRA_TRANSLATIONS).forEach(lang => {
  if (TRANSLATIONS[lang]) Object.assign(TRANSLATIONS[lang], EXTRA_TRANSLATIONS[lang]);
});

/* Lingue che usano il formato orario AM/PM (le altre usano 24h). */
const AMPM_LANGS = ['en', 'zh'];

/* Elenco lingue mostrate nel selettore */
const LINGUE = [
  { code: 'it', label: 'Italiano' },
  { code: 'en', label: 'English' },
  { code: 'fr', label: 'Français' },
  { code: 'es', label: 'Español' },
  { code: 'de', label: 'Deutsch' },
  { code: 'zh', label: '中文' },
  { code: 'pl', label: 'Polski' },
  { code: 'ru', label: 'Русский' },
  { code: 'pt', label: 'Português' }
];

window.LINGUE = LINGUE;
window.I18N = {
  lang: 'it',

  isAmpm() { return AMPM_LANGS.includes(this.lang); },

  /** Formatta "HH:MM" secondo la lingua: 24h oppure AM/PM. */
  formatTime(hhmm) {
    if (!hhmm) return '';
    const [h, m] = hhmm.split(':').map(Number);
    if (this.isAmpm()) {
      const ampm = h >= 12 ? 'PM' : 'AM';
      const h12 = h % 12 === 0 ? 12 : h % 12;
      return `${h12}:${String(m).padStart(2, '0')} ${ampm}`;
    }
    return `${String(h).padStart(2, '0')}:${String(m).padStart(2, '0')}`;
  },

  /** Traduzione di una chiave nella lingua corrente (fallback IT). */
  t(key) {
    const dict = TRANSLATIONS[this.lang] || TRANSLATIONS.it;
    return dict[key] !== undefined ? dict[key] : (TRANSLATIONS.it[key] || key);
  },

  /** Applica le traduzioni a tutta la pagina. */
  apply(lang) {
    this.lang = TRANSLATIONS[lang] ? lang : 'it';
    document.documentElement.lang = this.lang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
      el.innerHTML = this.t(el.getAttribute('data-i18n'));
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      el.placeholder = this.t(el.getAttribute('data-i18n-ph'));
    });
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
      el.setAttribute('aria-label', this.t(el.getAttribute('data-i18n-aria')));
    });
    try { localStorage.setItem('lingua', this.lang); } catch (e) {}
    document.dispatchEvent(new CustomEvent('linguaCambiata', { detail: this.lang }));
  },

  /** Lingua iniziale: preferenza salvata o lingua del browser, altrimenti IT. */
  init() {
    let scelta = 'it';
    try {
      const salvata = localStorage.getItem('lingua');
      if (salvata && TRANSLATIONS[salvata]) {
        scelta = salvata;
      } else {
        const navLang = (navigator.language || 'it').slice(0, 2).toLowerCase();
        if (TRANSLATIONS[navLang]) scelta = navLang;
      }
    } catch (e) {}
    this.apply(scelta);
    return scelta;
  }
};
